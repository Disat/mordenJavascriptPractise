// 遍历：forEach
// arr.forEach 方法允许为数组的每个元素都运行一个函数。

["Bilbo", "Gandalf", "Nazgul"].forEach((item, index, array) => {
    alert(`${item} is at index ${index} in ${array}`);
  });

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// arr.map 方法是最有用和经常使用的方法之一。
let lengths = ["Bilbo", "Gandalf", "Nazgul"].map(item => item.length);
alert(lengths); // 5,7,6
